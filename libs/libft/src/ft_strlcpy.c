#include "../headers/libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	source_size;
	size_t	i;

	source_size = 0;
	i = 0;
	while (src[source_size] != '\0')
		source_size++;
	if (size > 0)
	{
		while (src[i] != '\0' && i < (size - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	return (source_size);
}
